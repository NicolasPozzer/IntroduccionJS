const numero1 = 20 // number
const numero2 = "20" // string



// Comparador Simple ==
if(numero1 == numero2){
    console.log("Son iguales")
}else{
    console.log("No, no son iguales")
}
// Respuesta: "Son iguales"


// Comparador Estricto ===
if(numero1 === numero2){
    console.log("Son iguales")
}else{
    console.log("No, no son iguales")
}
// Respuesta: "No, no son iguales"


// Utilizar igual estricto es una buena practica